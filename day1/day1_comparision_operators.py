# Comparision Operators

# Comparisions

num_1 = 3
num_2 = 2

# Equal
print(num_1 == num_2)

# Not Equal
print(num_1 != num_2)

# Greater Than
print(num_1 > num_2)

# Less Than
print(num_1 < num_2)

# Greater or Equal
print(num_1 >= num_2)

# Less or Equal
print(num_1 <= num_2)


